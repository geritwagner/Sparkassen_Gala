    <h3>Startunterlagen/ Startnummern</h3>
    <p> Die Startunterlagen sind in der Regel vereinsweise gegen Entrichtung der Organisationsgebühren abzuholen. Die Ausgabe von Einzelnummern („mein Vereinsboss kommt erst später zum Bezahlen“ oder ähnliche Begründungen) sollte die Ausnahme sein.
      <br /> Die Startnummer ist im Wettkampf unverändert auf der Brust zu tragen. Sicherheitsnadeln sind vom Athleten/in selbst mitzubringen. Für „Vergissmeinnichte“ bietet der Ausrichter Viererpäckchen zum Preis von € 1.- an.
      <br /> Für sämtliche Läufe ab 800m werden zusätzlich Laufnummern unmittelbar vor dem Start ausgegeben, die unverändert auf der Brust zu tragen sind. Die anderen
      <span style="font-weight: bold">ausgegebenen Nummern</span> sind
      <span style="font-weight: bold">hier</span> unverändert
      <span style="font-weight: bold">auf dem Rücken</span> zu tragen.</p>

    <br />

    <h3>Startunterlagen wann und wo?</h3>
    <p> Diese gibt’s zu folgenden Zeiten:
      <br /> Ausgabe 04. Juni 2010 von 16.00 - 18.00 Uhr und 05. Juni 2010 ab 07.30 Uhr vereinsweise gegen Entrichtung der Organisationsgebühren oberhalb der Tribüne des Sportzentrums der Universität.</p>

    <br />

    <h3>Stellplatz</h3>
    <p> Wir bitten alle Teilnehmer/Innen ihre Stellpatzkarte pünktlich 75 Minuten vor dem Start am Stellplatz (hinter der Tribüne) abzugeben. Bitte die Stellplatzkarten nur abgeben, wenn der Athlet sicher am Wettkampf teilnimmt. Ansonsten gibt es freie Bahnen und das ist unfair gegenüber den anderen Teilnehmern.</p>

    <br />

    <h3>Ergebnisse</h3>
    <p> Die Ergebnisse werden schnellstmöglich nach Beendigung des jeweiligen Wettbewerbes an den dafür vorgesehenen Plätzen (Tribünenbereich) ausgehängt. Wir sind bemüht, die vollständigen Ergebnisse möglichst zeitnah unter der Adresse <a href="http://www.sparkassen-gala.de/"
      target="_blank">www.sparkassen-gala.de</a> ins Internet zu stellen. </p>

    <br />

