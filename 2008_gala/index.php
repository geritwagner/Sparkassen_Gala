<h2>Überzeugende Leistungen in der Oberpfalz </h2>
<br />
<p class="floatbox"><img src="photos/unger_t_regensburg08_foto_kiefner.jpg" width="120" height="171" class="float_left" /> Regensburg, 08. Juni 2008 (leichtathletik.de) – Ein zum Saisoneinstand starker Tobias Unger (LAZ Salamander Kornwestheim/Ludwigsburg) über 100 Meter, ein Doppelsieg der Bremerin Jonna Tilgner über 400 Meter und 400 Meter Hürden sowie der Höhenflug von Stabhochspringerin Silke Spiegelburg (TSV Bayer 04 Leverkusen) prägten am Sonntag die Sparkassen-Gala in Regensburg, die mit 3.500 Zuschauern eine bemerkenswert stimmungsvolle Kulisse zu bieten hatte. Tobias Unger erwischte am Nachmittag einen Saisoneinstieg nach Maß. Bereits im Vorlauf über die 100 Meter unterbot er in 10,17 Sekunden die erste Olympianorm (10,20 sec). Damit blieb der Sprinter bei nur leichtem Rückenwind (+ 0,1 m/sec) eine Hundertstel über seiner fast drei Jahre alten persönlichen Bestzeit (10,16 sec). Der 28-Jährige, der sich im Sommer vor allem auf die 100 Meter konzentrieren will, unterstrich somit eindrucksvoll seine Konkurrenzfähigkeit über die Kurzstrecke, auch für das mögliche Kräftemessen mit seinen kontinentalen Widersachern beim Europacup in Annecy (Frankreich; 21./22. Juni).    <a href="http://www.leichtathletik.de/index.php?NavID=1&amp;SiteID=28&amp;NewsID=17479&amp;Year=2008&amp;IsArchive=1" target="_blank">&raquo; weiter</a><br /> 
