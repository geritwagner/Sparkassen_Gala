<h2>Unger und Spiegelburg strahlen </h2>
<strong>Sprinter mit Olympianorm, Stabhochspringerin mit Bestleistung, Hütters Unfall</strong><br />
<br />
<p class="floatbox"><img src="photos/spiegelburg_galao8_mz-lex.jpg" width="185" height="171" class="float_left" /> Regensburg, 9. Juni 2008 (mz/wotruba) - Von Jahr zu Jahr etabliert sich die Spitzen-Leichtathletik mehr in Regensburg. Die stimmungsvolle Sparkassen-Gala der LG Telis Finanz sorgte gestern im Universitätsstadion vor 3500 Zuschauern wieder für Leistungen von nationalem, ja internationalem Format. Die bemerkenswertesten Auftritte legten Silke Spiegelburg und Tobias Unger hin. Die Stabhochspringerin überwand mit 4,65 Metern eine neue persönliche Bestleistung und wurde damit bisher weltweit 2008 nur von der Amerikanerin Jennifer Stuczynski und der Russin Yuliya Golubchikova übertroffen. Der Sprinter stieg mit 10,17 Sekunden über 100 Meter in die Saison ein und knackte damit auf Anhieb die Norm für die Olympischen Spiele in Peking, die bei 10,20 Sekunden liegt.  <a href="news/2008-06-09_mz-bericht_spiegelburg-unger.php">&raquo; weiter</a><br /> 
