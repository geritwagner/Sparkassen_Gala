<h2>Olympia-Aus für Hütter</h2>
<strong>Stabhochspringerin zieht sich Kreuzbandriss zu</strong><br />
<br />
<p>Regensburg, 09. Juni 2008   (mz/wotruba) – Stabhochspringerin Julia Hütter hat sich bei ihrem Unfall bei der   Gala in Regensburg einen Kreuzbandriss im linken Knie sowie   Knochenabsplitterungen im rechten Sprunggelenk zugezogen. Die 24 Jahre alte   deutsche Hallenmeisterin vom LAZ Bruchköbel fällt damit für die Olympia-Saison   aus. „Das ist tragisch für sie und ein Riesenverlust für den deutschen   Stabhochsprung“, sagte Bundestrainer Herbert Czingon. Hütter war bei leichtem   Regen und böigem Wind im dritten Versuch über 4,50 Meter abgestürzt und   ungebremst in den Einstichkasten gefallen. Wettkampf-Siegerin Silke Spiegelburg   (4,65 m) hatte schon gestern ihr Mitgefühl geäußert: „Das sah übel aus, vor   allem, als die Versorgung immer länger gedauert hat. Wir im Stabhochsprung   verstehen uns untereinander sehr gut. Ich werde mich unter der Woche mal bei ihr   melden.“ (dpa/cw)</p>

