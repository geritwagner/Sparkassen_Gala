<h2>Gala-Spezial mit Jennifer Wenth (1500m)</h2>
<p><strong>„Ich muss mit dem 1500er bei der Gala noch meinen Frieden schließen“</strong></p>
<p>Regensburg, 05. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng) - Jenni, im Jahr 2012 warst Du zum letzten Mal in Regensburg. Warum seitdem nicht 

mehr?</i><br /><br />

(Jennifer Wenth) - 2013 bin ich verletzungsbedingt ausgefallen und letztes Jahr war ich zwar nicht bei der Gala am Start, 

aber dafür bin ich bei den Bayerischen Meisterschaften außer Konkurrenz mitgelaufen: Ich habe 

damals mit Maren das 5000m-Limit für die EM geknackt.<br /><br /><br />

<i>Apropos Maren Kock: Das 1500m-Rennen bei der Gala wird ja gerade im Hinblick auf sie und ihre 

Regensburger Teamkollegin Corinna Harrer mit Hochspannung erwartet. Du kennst beide nicht nur 

aus Wettkampf und Training, sondern auch privat. Was unterscheidet die beiden?</i><br /><br />

Obwohl sie beide sehr erfolgreich sind, sind sie komplett am Boden geblieben und arbeiten im 

Training konzentriert an ihren Zielen. Ich sehe dabei aber vor allem Unterschiede beim Dauerlauf: Ein 

lockerer Dauerlauf mit Coco hat für mich nicht selten in einem flotten Tempo geendet – später 

wusste ich’s dann besser und bin mit Maren gelaufen: Ihr lockereres Tempo war für mich passender 

(lacht).<br /><br /><br />

<i>Die beiden möchten ja die WM-Norm in Angriff nehmen und stehen dementsprechend unter Druck. 

Wie ist das bei dir? Kannst Du einfach drauf loslaufen, oder hast auch du ein ganz konkretes Ziel vor 

Augen?</i><br /><br />

Da der Lauf es mein erster „ernsthafter“ 1500er seit dem Jahr 2011 ist, gehe ich doch sehr entspannt 

in das Rennen. Ich bin mir sicher, dass ich zu Beginn durch das hohe Tempo noch ein bisschen 

überfordert sein werde – das bin ich noch nicht gewohnt. Aber ich hoffe, dass ich dennoch gut ins 

Rennen finde. Ein Zeitziel habe ich allerdings keines, da meine Bestleistung noch aus meinen 

Mittelstrecklerinnen-Jahren stammt. Jetzt stehe ich ja im 5000m-Training und bin deshalb gespannt, 

was am Ende für eine Zeit herausschauen wird – und natürlich drücke ich den anderen Mädels die 

Daumen, dass sie die Norm packen!<br /><br /><br />

<i>Neben Maren und Corinna hast Du ja im vergangenen Ostertrainingslager in Cervia die gesamte 

Trainingsgruppe der LG Telis Finanz kennengelernt. Welche Erfahrungen hast Du gemacht?</i><br /><br />

Das Trainingslager war vor allem durch die positive Gruppendynamik super lehrreich und hat mich 

sportlich einen großen Schritt weitergebracht. LG-Coach Kurt Ring hat mich sofort in die Gruppe 

eingegliedert und ich habe einige Trainingsprogramme absolviert, die ich so noch nie gemacht hatte. 

Diese stehen jetzt teilweise auch zuhause auf dem Programm, da sie meinem Trainer Karl Sander gut 

gefallen haben.<br /><br /><br />

<i>Ist ein Gala-Start für dich dann schon fast zur Pflicht geworden – oder würdest Du selbst ohne die 

erfolgreiche Zusammenarbeit hierherkommen?</i><br /><br />

Natürlich freue ich mich in erster Linie darauf, alle Trainingskollegen aus Cervia  wiederzusehen. Ich 

wäre aber auch so zur Gala gekommen, da das Meeting ein wichtiger Wettkampf für den ÖLV 

(Österreichischer Leichtathletik Verband) ist. Außerdem muss ich mit dem 1500er bei der Gala noch 

meinen Frieden schließen, nachdem er 2012 aufgrund meiner Verletzung ein echtes Desaster war.</p>
