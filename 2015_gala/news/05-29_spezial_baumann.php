<h2>Gala-Spezial mit Jackie Baumann (400m Hürden)</h2>
<p><strong>„Ich will wieder mein Bestes zeigen“</strong></p>
<p>Regensburg, 29. Mai 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br /><br />
<i>(Franzi Reng) - Jackie, an Pfingsten hast Du mit einer neuen Bestzeit schon für einen fulminanten 

Saison-Einstieg gesorgt. Überwiegt momentan noch die Zufriedenheit oder wird jetzt erst richtig 

losgelegt?</i><br /><br />

(Jackie Baumann) - Gerade überwiegt noch die Zufriedenheit aber natürlich will ich in den nächsten Rennen auch wieder 

mein Bestes zeigen und mich hoffentlich noch weiter verbessern.<br /><br /><br />

<i>Am kommenden Samstag wirst Du mit Christiane Klopsch unter anderem eine Deutsche Meisterin als 

direkte Konkurrenz auf einer der Nebenbahnen haben. Willst Du sie gleich mal ein bisschen ärgern, 

oder liegt da deiner Meinung nach noch einiges dazwischen?</i><br /><br />

Ich finde es immer schön, gegen harte Konkurrenz zu laufen. Für mich bedeutet dies eigentlich 

immer einen zusätzlichen Ansporn. Natürlich ist Christiane eine Hausnummer in Deutschland,  ich 

werde sie vielleicht ein wenig ärgern können, wenn ich einen guten Tag habe. Aber das ist nicht mein 

Hauptaugenmerk in Regensburg.<br /><br /><br />

<i>Hast Du eigentlich noch nie auch mal mit etwas längeren Distanzen als den 400m geliebäugelt?</i><br /><br />

Nein eigentlich nicht. Ich wehre mich bis jetzt sehr erfolgreich gegen die 800m - auch wenn sehr viele 

Leute schon versucht haben, mich davon zu überzeugen.<br /><br /><br />

<i>Als Baumann-Tochter eilt einem ja doch immer der Ruf des bekannten Vaters voraus. Stört Dich das 

eigentlich?</i><br /><br />

Nein. Ich bin stolz auf das, was mein Vater erreicht hat und dass ich seinen Namen trage. Für mich ist 

er ein Vorbild, dem es nachzueifern gilt. Trotzdem finde ich es schön, an meinen eigenen Erfolgen 

gemessen zu werden und nicht an denen meines Vaters.</p>
