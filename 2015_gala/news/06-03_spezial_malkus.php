    <h2>Gala-Spezial mit Lena Malkus (Weitsprung)</h2>
    <p>
      <strong>„Zur Einstimmung schaue ich mir gerne die anderen Wettbewerbe an“</strong>
    </p>
    <p>
      <img class="float_left" title="Lena Malkus (Foto:Kiefner)" src="http://www.sparkassen-gala.de/2014/photos/kiefner/36.jpg" alt="Lena Malkus (Foto:Kiefner)" height="200" />Regensburg, 03. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.
      <br />
      <i>(Franzi Reng) - Zunächst einmal herzlichen Glückwunsch zu Deinem Sieg beim Kurpfalz-Meeting in

Weinheim! 6,94m – Das ist gleich persönliche Bestleistung so früh am Saisonanfang. War das zu

erwarten?</i>
      <br />
      <br /> (Lena Malkus) - Um ehrlich zu sein: Nein. Es hat sich im Training auf jeden Fall angedeutet, dass ich gut drauf bin, daher habe ich schon auf die WM-Norm von 6,70m geschielt. Aber dass es so weit geht - und das so früh in der Saison - damit habe
      ich nicht gerechnet.
      <br />
      <br />
      <br />

      <i>Was nimmst Du Dir nach diesem tollen Sprung jetzt noch für die Sparkassen Gala vor?</i>
      <br />
      <br /> Ich weiß, dass man solche Weiten nicht immer springen kann. Da muss schon einiges zusammenkommen. Daher ist das Ziel für alle weiteren Wettkämpfe, Weiten um die 6,60m - also die zweite DLV WM-Norm - zu stabilisieren.
      <br />
      <br />
      <br />

      <i>Was gehört für Dich zu einem guten Wettkampf? Brauchst Du vor allem starke Konkurrenz, die Dich

zu tollen Weiten treibt oder eher ein Publikum, das ordentlich anfeuert?</i>
      <br />
      <br /> So eindeutig kann ich gar nicht sagen, was mir lieber ist. Eine Mischung aus beidem ist eigentlich immer schön. Aber in Regensburg schätze ich die Bedingungen dafür als recht gut ein: Ich habe positive Erinnerungen an die Veranstaltung, da meist
      super Wetter und tolle Stimmung an der Grube ist. Ich freue mich auf jeden Fall auf den Wettkampf!
      <br />
      <br />
      <br />

      <i>Der Weitsprung der Frauen ist erst für 19.15 Uhr angesetzt. Wie vertreibst Du Dir die Zeit bis zum

Start?</i>
      <br />
      <br /> Natürlich ist es nicht leicht die Spannung für einen Wettkampf bis abends zu halten. Aber so viele Gedanken mache ich mir darüber gar nicht. Vormittags werde ich mich eher zurückziehen, aber am Nachmittag will ich schon im Stadion sein, um mir
      ein paar Wettbewerbe anzugucken. Zur Einstimmung auf meinen eigenen Wettkampf mache ich das ganz gerne. Außerdem werde ich mit meinen Trainingskolleginnen vor dem Weitsprungwettbewerb auch noch in der Staffel starten.</p>
