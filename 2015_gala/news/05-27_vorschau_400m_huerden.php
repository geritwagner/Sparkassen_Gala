<h2>Fällt über 400m Hürden die 50-Sekunden-Marke?</h2>
<p><strong>Im Regensburger Uni-Stadion ist schon so manche Norm geknackt worden</strong></p>
<p><img class="float_left" title="Silvio Schirrmeister (Foto:Kiefner)" src="http://www.sparkassen-gala.de/2014/photos/kiefner/19.jpg" alt="Silvio Schirrmeister (Foto:Kiefner)" width="180" />Regensburg, 27. Mai 2015 (orv) – Im letzten Jahr glänzte bei der Sparkassen
Gala der spätere Europameister Kariem Hussein aus der Schweiz mit 49,33sec
und zog dabei den Briten Cristian Morton (49,84sec) und Silvio Schirrmeister
(49,85sec - LAC Erdgas Chemnitz) unter die 50-Sekunden-Barriere. 2015 könnte
es erneut Silvio Schirrmeister sein, der die Schallmauer am 6. Juni
durchbricht. Der Chemnitzer hat in diesem Jahr immerhin schon eine 50,06
stehen. Ihm dicht auf den Fersen ist der ebenfalls für Regensburg gemeldete
Georg Fleischhauer (LG Eintracht Frankfurt – 50,49sec). Harte Konkurrenz
kommt aus Algerien: Lahoulou Abdelmalik ist schon 50,15sec gelaufen und auch
ein Aspirant auf eine 49er Zeit. Nicht vergessen sollte man den Münchner
Tobias Giehl mit einer Vorjahresbestleistung von 50,23sec, gelaufen bei den
nationalen Titelkämpfen in Ulm.<br /><br />

<img class="float_right" title="Christiane Klopsch (Foto:Kiefner)" src="http://www.sparkassen-gala.de/2014/photos/kiefner/22.jpg" alt="Christiane Klopsch (Foto:Kiefner)" width="180" />Bei den Frauen führt die Deutsche Meisterin Christiane Klopsch (LG ovag
Friedberg-Fauerbach) mit einer am Pfingstmontag gelaufenen 56,92sec die
Nennliste an. Dahinter taucht schon ein prominenter Name auf. Jackie
Baumann, Tochter von Olympiasieger Dieter Baumann und gecoacht von Mama
Isabelle, in der Leichtathletik wahrlich keine Unbekannte, überraschte beim
Pfingstmeeting in Rehlingen mit einer eklatanten Steigerung auf 57,39sec,
noch nicht im direkten Vergleich mit Christiane Klopsch. In Regensburg
werden sie nebeneinander die Bahn runterstürmen. Mal sehen, wer dann die
Nase vorne hat. Jackie Baumann ist jedenfalls jetzt schon eine heiße
Kandidatin für die U23-EM im Juli. Vielleicht gelingt auch Luisa Valeske (SV
schlau.com Saar 05 Saarbrücken) und Anna Raukuc (LG Hannover) der Sprung in
den 57er Bereich.</p>
