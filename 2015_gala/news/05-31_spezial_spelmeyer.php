<h2>Gala-Spezial mit Ruth Sophia Spelmeyer (400m)</h2>
<p><strong>„Ich wage mich erstmals wieder über die 400m-Distanz“</strong></p>
<p><img class="float_left" title="Ruth Sophia Spelmeyer (Foto:Kiefner)" src="http://www.sparkassen-gala.de/2014/photos/kiefner/27.jpg" alt="Ruth Sophia Spelmeyer (Foto:Kiefner)" width="180" />Regensburg, 30. Mai 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng) - Ruth, im letzten Jahr hast Du in Regensburg ja schon eine Top-Leistung gleich zum 

Saisonbeginn abgeliefert. Hattest Du damit gerechnet?</i><br /><br />

(Ruth Sophia Spelmeyer) - Nein, das hätte ich so nicht gedacht. Das war ja eines der allerersten 400m-Rennen des Sommers und 

ich war natürlich sehr froh, dass ich gleich so gut an meine Bestzeit vom vorherigen Jahr heranlaufen 

konnte.<br /><br /><br />

<i>Bist Du wegen dieses Erfolges heuer wieder am Start, oder gibt es da noch andere Beweggründe, die 

für die Sparkassen Gala sprechen?</i><br /><br />

Ich komme auch nach Regensburg, da der Wettkampf ja unter anderem entscheidend für die 

anstehende Team-EM ist - und natürlich wegen der stets guten Besetzung des Meetings.<br /><br /><br />

<i>In diesem Jahr wartet ja Esther Cremer als besonders starke Gegnerin auf Dich. Welche Bedeutung 

hat das für Dich?</i><br /><br />

Ich kenne Esther persönlich sehr gut. Ich weiß, dass sie mit einer 51-er Bestzeit wirklich was drauf hat 

und das ist ja auch mein Ziel für diese Saison. Da spielt es momentan aber noch gar keine große Rolle, 

wer von uns beiden dann die Nase vorne hat. Hauptsache die Zeiten stimmen!<br /><br /><br />

<i>Die meisten Deiner Konkurrenten sind ja spätestens zu den Pfingst-Meetings ins Renngeschehen 

eingestiegen. Warum haben wir Dich seit der Hallen-EM in Prag nicht mehr auf der Bahn gesehen?</i><br /><br />

Ich habe mir leider vor etwa zwei Wochen ein Band im Sprunggelenk angerissen. Ich werde an 

diesem Wochenende erst einmal in Hannover über die ganz kurzen Sprintstrecken antesten und mich  

in Regensburg dann erstmals wieder auf die 400m-Distanz wagen. Das wird sicherlich schwer, aber 

ich gebe natürlich mein Bestes.</p>
