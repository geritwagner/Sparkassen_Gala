<h2>Gala-Spezial mit Erik Balnuweit (110m Hürden)</h2>
<p><strong>„Vielleicht klappt es ja mit der WM-Norm“</strong></p>
<p><img class="float_left" title="Florian Orth (Foto:Kiefner)" src="../images/inhalt/Orth_Florian_BLV-Langstaffel_2015_Kiefner-Foto.jpg" alt="Florian Orth (Foto:Kiefner)" height="220" />Regensburg, 04. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng) Erik, letzte Woche bist Du schon beim Kurpfalz-Meeting in Weinheim gestartet. Für Dich 

ein zufriedenstellender Saisonauftakt?</i><br /><br />

Mit meinem Saisoneinstieg bin ich nicht so zufrieden. Derzeit läuft es noch nicht ganz rund. Die 

Vorbereitung verlief nicht so wie gewünscht, aber wir versuchen im Team weiter an gewissen Details 

zu feilen, damit es in den nächsten Rennen und auch in Regensburg wieder schneller wird. Das Ziel ist 

in Regensburg, die Saisonbestleistung weiter zu steigern und Rennerfahrung zu sammeln.<br /><br /><br />

<i>Eine persönliche Bestzeit von 13,44s und eine WM-Norm bei 13,45s – Das klingt doch nach einer 

machbaren Aufgabe, oder? Denkst Du, dass Du am nächsten Wochenende schon die Qualifikation 

abhaken kannst? </i><br /><br />

Die WM-Norm ist auf jeden Fall kein Selbstläufer, weil sie doch recht hoch angesetzt ist. Ich bin aber 

der Meinung, dass diese Zeit aber auf jeden Fall zu erreichen ist, da diese Norm eben auch über 

meiner Bestleistung liegt. Ob das jedoch schon in Regensburg gelingt, wird sich dann zeigen. 

Vielleicht klappt es ja!<br /><br /><br />

<i>Mit Wayne Davis wartet in Regensburg ja sogar ein 13,20s-Mann auf Dich. Kennst Du ihn?</i><br /><br />

Wayne ist ein sehr guter Athlet, der schon viele starke Rennen gezeigt hat. Persönlich kenne ich ihn 

nicht, aber es wird ein spannendes Rennen um den Sieg. Ich freue mich drauf! Grundsätzlich kann ja 

jeder von starker Konkurrenz profitieren.<br /><br /><br />

<i>Aber auch die Konkurrenz aus Deutschland schläft nicht. Wen der Starter im Gala-Feld schätzt Du hier 

besonders stark ein? Kann Dir da jemand gefährlich werden?</i><br /><br />

Wir haben da eine Vielzahl guter Hürdensprinter in Deutschland. Am stärksten einzuschätzen ist bei 

diesem Starterfeld derzeit Alexander John, weil er nicht nur aufgrund seiner Bestleistung schnelle 

Rennen zeigen kann.</p>
