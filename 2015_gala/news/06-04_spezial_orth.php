    <h2>Gala-Spezial mit Florian Orth (1500m)</h2>
    <p>
      <strong>„Schnelle Temporennen oder Taktik mit Spurt – das macht Spaß“</strong>
    </p>
    <p>
      <img class="float_left" title="Florian Orth (Foto:Kiefner)" src="../images/inhalt/Orth_Florian_BLV-Langstaffel_2015_Kiefner-Foto.jpg" alt="Florian Orth (Foto:Kiefner)" height="220" />Regensburg, 04. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.
      <br />
      <i>(Franzi Reng) - Flo, zunächst 3x1000m-Staffel, dann 3000m beim Rolf-Watter-Meeting und zuletzt

sogar ein Start über 5000m in Belgien. Du begibst Dich in der letzten Zeit anscheinend gern auf

fremdes Terrain. Wie kommt’s?</i>
      <br />
      <br /> (Florian Orth) - Naja, so fremd ist das Terrain ja gar nicht. In der diesjährigen Hallensaison bin ich bei der Hallen-EM in Prag ja auch schon über 3000m unterwegs gewesen und 3x1000m Staffel laufe ich immer gerne mit unserem Team, wenn Sie zustande kommt. Gut, die 5000m sind für mich wirklich noch fremdes Terrain. Bei meinem Debüt in Belgien war ich absoluter Neuling auf dieser Strecke, aber das ist mir - glaube ich - ganz gut gelungen (lacht) Viele sehen hier ja auch meine Zukunft. Für mich
      ist es erstmal nur eine Option mehr.
      <br />
      <br />
      <br />

      <i>Bei der Gala sollen es dann aber doch wieder die guten alten 1500m sein. Bist Du erleichtert, oder hat

der kleine „Ausflug“ in die anderen Distanzen auch ganz gut getan?</i>
      <br />
      <br /> Natürlich freue ich mich auf die 1500m in Regensburg, das ist - noch - meine Lieblingsstrecke: Schnelle Temporennen oder Taktik mit Spurt, in jedem Fall Laktat für den Athleten und kurzweilig für die Zuschauer, das macht Spaß! Und doch haben mir auch die 5000m gut getan, weil mir das Rennen gezeigt hat, was in der Zukunft noch möglich ist.
      <br />
      <br />
      <br />

      <i>Mit Tempomachern, starken Gegnern und einem großartigen Regensburger Publikum, das natürlich

am liebsten seine Lokalmatadoren anfeuert ist eigentlich alles angerichtet für eine WM-Norm. Ist das

nächsten Samstag schon das erklärte Ziel für Dich?</i>
      <br />
      <br /> Die WM-Norm ist im Hinterkopf natürlich immer das erklärte Ziel, aber ob das schon in Regensburg klappt, muss man abwarten. Dass das Regensburger Publikum im Anfeuern großartig sein wird, davon bin ich überzeugt. Mal schauen, was Feld und Wetterbedingungen am Samstagabend hergeben. Außerdem bin ich gespannt, wie gut ich meinen ersten 5000m-Lauf auf der Bahn wegstecken konnte: Die eine oder andere Trainingseinheit danach fühlte sich nämlich doch noch recht zäh an.
      <br />
      <br />
      <br />

      <i>Angenommen Feld und Wetterbedingungen sind am Samstagabend auch noch perfekt: Warum ist die

WM-Normjagd trotz all dem kein leichtes Unterfangen?</i>
      <br />
      <br /> Theoretisch ist die Aufgabe durchaus lösbar: Meine Bestzeit über 1500m liegt mit 3:34,54 knapp eine Sekunde unter der WM-Norm für Peking von 3:35,50. Aber Theorie und Praxis liegen doch oft weit auseinander: Ich kann mich noch sehr gut an meine Rennen unter der WM-Norm erinnern, solche muss man erstmal wieder erwischen. Da kann eine Sekunde ganz schön lang oder auch mal ganz schön kurz sein.
      <br />
      <br />
      <br />

      <i>Warum sollte jeder Regensburger Leichtathletik-Fan am Samstagnachmittag zwischen 19 Uhr und

20:30 Uhr im Unistadion sein? Wie stehen Deiner Meinung nach die Chancen auf eine "Blaue

Stunde"?</i>
      <br />
      <br /> Leichtathletik auf diesem Niveau und die Regensburger "local heros" mittendrin - das kann man nicht jeden Tag erleben und sollte man sich auch nicht entgehen lassen. Die "Blaue Stunde" hatten wir bisher ja nur „auswärts“ bei den Meisterschaften.
      Beim Heimspiel "Sparkassen Gala" ist es noch etwas Besonderes: Maren Kock und Coco Harrer wollen auf gleicher Strecke wie ich die WM-Norm anpeilen, Bene Huber über 800m die Norm für die Universiade - was ich übrigens allen drei zutraue- Hoffen wir das Beste! (lacht)</p>
