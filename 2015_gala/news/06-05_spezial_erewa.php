    <h2>Gala-Spezial mit Robin Erewa (100m, 200m)</h2>
    <p>
      <strong>„100 Meter bleiben 100 Meter“</strong>
    </p>
    <p>Regensburg, 05. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.
      <br />
      <i>(Franzi Reng) - Robin, vor zwei Jahren bist Du in Regensburg über 200 Meter an den Start gegangen,

heuer sollen es die 100 Meter sein – was ist der Unterschied zwischen den beiden Distanzen für Dich?

Welche Strecke ist Dir lieber?</i>
      <br />
      <br /> (Robin Erewa) - Ich laufe dieses Jahr sogar beides. 100 Meter liegen mir leider nicht so, allerdings kann man 200 auch nur gut rennen, wenn die 100 schnell sind. Deshalb arbeiten wir daran, auch immer wieder diese Strecke bei mir zu verbessern.
      <br
      />
      <br />
      <br />

      <i>Selbst wenn man noch ein bisschen tiefer in den Archiven der Sparkassen-Gala sucht, taucht bereits

im  Jahr 2010 Dein Name in den Ergebnislisten auf. Könnte man sagen Du bist mit dem Meeting groß

geworden?</i>
      <br />
      <br /> Das könnte man tatsächlich so sagen. Das Meeting wird immer gern von Bundestrainern genommen, um Staffeln aufzustellen. Dadurch habe ich natürlich versucht, den Wettkampf so oft es ging, wahrzunehmen. So ist die sechs Stunden lange Reise von Bochum
      in den Süden Deutschlands für mich immer lohnend gewesen.
      <br />
      <br />
      <br />

      <i>Warum warst Du dann im vergangenen Jahr nicht am Start?</i>
      <br />
      <br /> Letztes Jahr bin ich aus einer Verletzung in die Saison gestartet und wollte meinem Beuger die – wie bereits erwähnt - sehr lange Fahrt vor einem Wettkampf dann doch noch nicht zumuten.
      <br />
      <br />
      <br />

      <i>Im letzten Jahr wurde das 100m-Rennen wegen des Gegenwinds sogar in die Gegenrichtung verlegt. 

Wäre es für Dich eine große Umstellung, wenn es in diesem Jahr wieder so kommen würde?</i>
      <br />
      <br /> 100 Meter bleiben 100 Meter! Ich finde es gut, dass sich der Veranstalter darauf einlässt, die Bedingungen Athleten freundlich zu gestalten. Ich hätte kein Problem damit, andersherum zu laufen.</p>
