    <h2>Gala-Spezial mit Georg Fleischhauer (400m Hürden)</h2>
    <p>
      <strong>„Ich will mit nach Cheboksary“</strong>
    </p>
    <p>Regensburg, 30. Mai 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.
      <br />
      <i>(Franzi Reng) - Georg, die letzten Jahre gab es in Regensburg ja immer viel starke Konkurrenz über die

400m-Hürden für Dich. Warst Du mit Deinen Leistungen zufrieden?</i>
      <br />
      <br /> (Georg Fleischhauer) - Nein, in den letzten Jahren war ich eher nicht zufrieden, weil ich meine Leistungen nicht meinen Vorstellungen entsprochen hatten. Dieses Jahr möchte ich endlich wieder an meine alte Stärke anknüpfen und Entsprechendes abliefern.
      <br
      />
      <br />
      <br />

      <i>Wie ist Deine Saisonvorbereitung bisher verlaufen? Fühlst Du Dich nach Deinem Saisoneinstieg

in Pliezhausen und München schon fit für einen starken Sommer, oder muss da noch ein bisschen was

getan werden?</i>
      <br />
      <br /> Nach den ersten Rennen bin ich nicht zufrieden mit meinem Wettkampfeinstieg. Das hat nicht im Ansatz meinen Trainingsleistungen entsprochen, denn trainiert habe ich eigentlich genug und sehr gut - jetzt muss es nur noch im Wettkampf klappen.
      <br
      />
      <br />
      <br />

      <i>Ist Regensburg für Dich also noch ein Aufbau-Rennen zur Formüberprüfung, oder geht es da schon

richtig zur Sache?</i>
      <br />
      <br /> Regensburg ist definitiv schon mehr als eine Formüberprüfung. Die Gala ist der letzte Wettkampf vor der Team-EM und damit besonders wichtig für die Qualifikation. Ich will mit nach Cheboksary (Team- EM) und dafür werde ich schon eine ordentliche
      Zeit anbieten müssen!
      <br />
      <br />
      <br />

      <i>Letztes Jahr kam bei dem guten Gala-Wetter Deine Sonnenbrille – mittlerweile ja schon ein

Markenzeichen von Dir –erneut zum Einsatz. Setzt Du sie eigentlich auch auf, wenn der Sommer noch

ein bisschen auf sich warten lässt und Regensburg nur einen Wolkenhimmel bieten kann?</i>
      <br />
      <br /> Die Brille trage ich natürlich vor allem bei Sonnenschein, aber mittlerweile auch einfach als Schutz für die Augen gegen Wind oder Insekten. Weiterhin bietet sie eine Art Rückzugsmöglichkeit während der psychisch doch etwas belastenden Situation
      vor dem Wettkampf. Dieses Jahr bin ich mir noch nicht ganz sicher, wie ich es handhabe - ich werde das sicher immer von der Situation im Wettkampf selbst abhängig machen.</p>
