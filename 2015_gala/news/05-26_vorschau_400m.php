<h2>Die 400m haben bei der Sparkassen Gala Tradition</h2>
<p><strong>Seit Jahren gibt sich im Uni-Stadion Deutschlands Elite die Ehre</strong></p>
<p><img class="float_left" title="Kamghe Gaba (Foto:Kiefner)" src="http://www.sparkassen-gala.de/2014/photos/kiefner/24.jpg" alt="Kamghe Gaba (Foto:Kiefner)" width="180" />Regensburg, 26. Mai 2015 (orv) – Manche sagen, in Regensburg hat man 300m
Rückenwind. Inzwischen hat das Rund im Uni-Stadion bei Deutschlands
Viertelmeiler/Innen einen legendären Ruf. Pünktlich Anfang Juni zum
Zeitpunkt der Sparkassen Gala sind sie da und lassen so manchen Patzer der
ersten Saisonrennen vergessen. Angeführt vom Deutschen Meister Kamghe Gaba
(LG SW München), der als einziger mit 45,63sec einer 45er-Zeit mitbringt,
wollen sich Thomas Schneider (SC Magdeburg – 46,08), Miguel Rigau (LT DSHS
Köln – 46,13), David Gollnow, Johannes Trefz, Benedikt Wiesend (alle LG SW
München –46,36/46,50/46,99) für die 4x400m-Staffel, die Deutschland bei der
Team-EM in Russland vertreten wird, empfehlen. Bundestrainer Volker Beck
wird sie gleich im Anschluss der Sparkassen Gala auf Grund der Regensburger
Ergebnisse nominieren. Gefordert werden Deutschlands Stars vom Briten Jamie
Bowie, der mit seinen 46,18sec auch schon an den 45 Sekunden gekratzt hat
und von Italiener Mario di Giambattista.<br/> <br/>

Bei den Frauen ergibt sich ein ähnliches Bild. Esther Cremer (TV
Wattenscheid) sticht mit ihren 51,87sec deutlich heraus, gefolgt von Ruth
Sophia Spelmeyer (VfL Oldenburg), die voriges Jahr exakt bei der Sparkassen
Gala ihre persönliche Bestzeit von 52,63sec aufstellte. Laura Müller (LSG
Saarbrücken/Sulzbachtal – 52,91), die Deutsche Langhürden-Meisterin
Christiane Klopsch (LG ovag Friedberg-Fauerbach – 52,99) – sofern sie nicht
die 400m Hürden vorzieht - , Janin Lindenberg (SC Magdeburg – 53,17) und
Friederike Möhlenkamp (LT DSHS Köln) werden dann wohl die weiteren
Staffelplätze für die Team-EM unter sich ausmachen.</p>
