<h2>Gala-Spezial mit Thomas Schneider (400m)</h2>
<p><strong>„Für mich war Regensburg immer eine Reise wert“</strong></p>
<p>Regensburg, 04. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng) - Wenn man mal ein bisschen länger zurückschaut, findet man Dich schon in den 

Ergebnislisten der Gala aus dem Jahr 2010. Hast Du noch Erinnerungen an damals?</i><br /><br />

(Thomas Schneider) - Für ein paar Erinnerungen musste ich mir ehrlich gesagt nochmal kurz das Video anschauen, um mein 

Gedächtnis aufzufrischen. In meiner Erinnerungen sind eher Ergebnisse als genaue Jahreszahlen 

vorhanden. (lacht) Es war allerdings ein ziemlich gutes Rennen – für mich war Regensburg eigentlich 

immer eine Reise wert und ich freue mich jedes Jahr auf die Gala.<br /><br /><br />

<i>Denkst Du, dass sich das Meeting seitdem in irgendeiner Weise verändert hat, oder kann man sich da 

auf Altbekanntes verlassen?</i><br /><br /> 

Aus meiner Sicht hat sich seit 2010 nicht viel geändert - das ist durchaus positiv zu verstehen. Die 

Organisation ist super, das Feld immer hochklassig und das Publikum sehr fachkundig!<br /><br /><br />

<i>Im letzten Jahr hast Du die Regensburger Sparkassen Gala nach Deinem letzten Start 2013 erst einmal 

ausgelassen. Warum?</i><br /><br />

Letztes Jahr hat mich eine Grippe an einem Start gehindert. Da die letzten Jahre gezeigt haben, dass 

man in Regensburg immer schnell rennen kann, war ich daher auch ziemlich enttäuscht.<br /><br /><br />

<i>Mit dem amtierenden Deutschen Meister Kamghe Gaba ist heuer ja eigentlich alles angerichtet für 

ein absolutes Top-Rennen. Geht es da hauptsächlich um schnelle Zeiten, oder spielt der direkte 

Vergleich mit der restlichen deutschen Spitze auch eine Rolle für Dich?</i><br /><br />

Ich freue mich einfach auf den Wettkampf. Zeiten und die Position im nationalen Vergleich sind für 

mich eher zweitrangig. Ich will auf der Stadionrunde Spaß haben und meinen Bestes geben. Alles 

andere regelt sich dann von selbst!</p>
