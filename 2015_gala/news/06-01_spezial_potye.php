<h2>Gala-Spezial mit Tobias Potye (Hochsprung)</h2>
<p><strong>„Die Form ist da, nur die Routine fehlt“</strong></p>
<p>Regensburg, 31. Mai 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng) - Tobias, im letzten Jahr ging es im Hochsprung der Männer ja nicht bis zur 2,20m-Höhe.

Du als damals noch U20-Jugendlicher hast mit Deinen 2,22m die Großen deswegen schon das 

Fürchten gelehrt. Heuer wirst Du in der Männer-Wertung sicherlich auch den Gesamtsieg in Angriff 

nehmen, oder?</i><br /><br />

(Tobias Potye) - Letztes Jahr war Regensburg ein Saison-Highlight für mich. Die 2,22 m waren auf jeden Fall eine 

Bestätigung meiner Topform und haben bestimmt einen guten Eindruck hinterlassen. Dieses Jahr 

habe dafür ich in der unmittelbaren Vorbereitung mit einer Verletzung zu kämpfen. Das heißt aber 

nicht, das ich mich deshalb davor schon geschlagen gebe: Die Form ist ja nach meinen 

Trainingslagern in Südafrika und der Türkei da, nur die Routine fehlt - ich spüre das!<br /><br /><br />

<i>Als 20-jähriger gehörst du ja trotzdem immer noch zu den „Jungen Wilden“ im Starterfeld. Siehst Du 

das eher als Vor- oder Nachteil?</i><br /><br />

Zwar haben ältere Athleten mehr Erfahrung und Training, sie sind manchmal aber auch 

verletzungsanfälliger. Die Erwartungen an uns junge Athleten sind dafür sehr hoch, aber wir haben ja 

auch noch einiges vor uns. Jung sein ist in meinen Augen also auf jeden Fall ein Vorteil: Wir können 

unser eigenes Ding machen, freuen uns über die Platzierungen - letztlich geht es trotzdem gerade nur 

um die Qualifikation für Tallinn!<br /><br /><br />

<i>Du sprichst schon über die U23-EM in Estland: Peilst Du die Norm dafür in Regensburg schon an? Wird 

bei der Gala der erste Versuch gestartet?</i><br /><br />

Leider ist die Gala mein erster und etwas knapp angesetzter Versuch für die Quali. Ich habe einen 

Wettkampf wegen meiner Knie-Verletzung vor zwei Wochen schon verschenkt. Jetzt zählen 

Regensburg und Wetzlar. Klar wird die Chance ernst genommen!<br /><br /><br />

<i>Für bayerische Leichtathleten ist Regensburg ja eines der „naheliegendsten“ Meetings. Gibt es außer 

der kurzen Distanz zur Heimat für Dich noch andere Gründe, die für einen Start in der Domstadt 

sprechen?</i><br /><br />

Die letzten Jahre war es schön warm und eine tolle Stimmung. Da macht es glatt mehr Spaß zu 

springen! Es ist aber auch eine „Pflichtveranstaltung“ für Kaderathleten. Bisher habe ich nur gute 

Erfahrungen in Regensburg gemacht - ich hoffen das bleibt so!</p>
