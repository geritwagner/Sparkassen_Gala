<h2>Gala-Spezial mit Silvio Schirrmeister (400m Hürden):</h2>
<p><strong>„Man kommt wieder in den Race-Modus“</strong></p>
<p>Regensburg, 30. Mai 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng)Im letzten Jahr hast Du schon über eine Zeit unter der 50-Sekunden-Marke jubeln 

können. Ist das heuer auch drin?</i><br /><br />

Mein Anspruch ist es auf jeden Fall eine 49 vor das Komma zu laufen. Vor allem möchte ich aber 

meine eigene Saisonbestleistung verbessern!<br /><br /><br />

<i>Oder ist am Ende vielleicht noch mehr drin? Wie fühlst Du Dich in den letzten Trainingseinheiten vor 

dem Meeting in Regensburg?</i><br /><br />

Nach den vorangegangen Trainingseinheiten und dem Trainingslager in der USA bin ich einfach froh, 

dass es endlich  mit den Wettkämpfen losgeht -  man kommt wieder in den "Race Modus"!<br /><br /><br />

<i>Dicht im Nacken sitzt Dir beispielweise Georg Fleischhauer, der sicherlich ähnliche Ziele im Blick hat. 

Spielt es für Dich eine Rolle wer letztendlich die Nase vorn hat, oder geht es hauptsächlich um die Zeit, 

die zum Schluss zu Buche steht?</i><br /><br />

Zunächst geht es darum, dass jeder sein eigenes Rennen macht und eine gute Zeit rauskommt  - wer 

dann letztlich die Nase vorn hat, wird sich zeigen (lacht)<br /><br /><br />

<i>2015 ist ja bei Weitem nicht Dein erster Auftritt in Regensburg. Warum ist die Domstadt für Dich 

immer eine Reise wert?</i><br /><br />

In Regensburg ist die Anlage einfach Top und die Organisation des Meetings ist sehr angenehm. Die 

Gala ist deswegen schon seit 2009 ein wichtiger Bestandteil in meinem Wettkampfplan.</p>
