    <h2>Gala-Spezial mit Manuel Leitner (Weitsprung)</h2>
    <p>
      <strong>„In Regensburg herrscht eine Wahnsinns-Stimmung!“</strong>
    </p>
    <p>Regensburg, 04. Juni 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.
      <br />
      <i>(Franzi Reng) - Manuel, Du bist gerade noch auf Teneriffa. Geht es von dort aus direkt nach

Regensburg, oder vorher nochmal kurz in die Heimat?</i>
      <br />
      <br /> (Manuel Leitner) - Es geht noch mal kurz in die Heimat. Dort absolviere ich noch zwei Trainingseinheiten mit meinem Trainer Wolfgang Adler, danach geht die Reise dann nach Regensburg
      <br />
      <br />
      <br />

      <i>Hast Du auf der Insel ununterbrochen trainiert, oder kommt man da nicht in Versuchung, auch ein

bisschen die Seele baumeln zu lassen?</i>
      <br />
      <br /> Das Entspannen ist definitiv nicht zu kurz gekommen. Dafür habe ich aber die Trainingseinheiten ein wenig intensiver gestaltet. Regensburg soll ja ein erfolgreicher Wiedereinstieg nach meiner langen Verletzungspause werden. Wenn ich schmerzfrei
      und locker 7-Meter-Sprünge schaffe, wäre ich schon sehr zufrieden!
      <br />
      <br />
      <br />

      <i>Als Österreicher vom PSV Hornbach Wels kann man Dich ja quasi schon als internationale Konkurrenz

bezeichnen. Hat sich das Regensburger Meeting in Deiner Heimat bereits einen großen Namen

gemacht, oder ist es eher noch ein Geheimtipp unter Insidern?</i>
      <br />
      <br /> In der Weitsprungszene hat es wegen der guten Anlage schon einen Namen. Aber auch der Nationalkader nimmt das Meeting gerne mit, da die Bahn ebenfalls sehr schnell ist. Außerdem herrscht in Regensburg immer eine Wahnsinns-Stimmung!
      <br />
      <br />
      <br />

      <i>In Regensburg bekommst Du es ja heuer auch viel mit Konkurrenz aus noch entfernteren Ländern wie

Ghana oder Großbritannien zu tun. Kennt man sich da überhaupt untereinander?</i>
      <br />
      <br /> Ja doch. Man lernt sehr viele Athleten an Wettkämpfen kennen und bleibt auch mit ihnen in Kontakt, zum Beispiel über Facebook. Mit manchen mache ich auch ab und zu ein paar Trainingseinheiten – man arbeitet einfach zusammen.</p>
