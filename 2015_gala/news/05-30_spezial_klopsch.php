<h2>Gala-Spezial mit Christiane Klopsch (400m Hürden)</h2>
<p><strong>„Ich führe momentan ein Doppelleben“</strong></p>
<p><img class="float_left" title="Christiane Klopsch (Foto:Kiefner)" src="http://www.sparkassen-gala.de/2014/photos/kiefner/22.jpg" alt="Christiane Klopsch (Foto:Kiefner)" width="180" />Regensburg, 30. Mai 2015 (Franzi Reng) - Im Laufe der nächsten Tage werden wir an dieser Stelle immer wieder Interviews mit Startern der diesjährigen Gala veröffentlichen.<br />
<i>(Franzi Reng) - Christiane, man sagt ja, auf der Rundbahn in Regensburg hätte man 300 Meter 

Rückenwind. Kannst Du das aus eigener Erfahrung bestätigen?</i><br /><br />

(Christiane Klopsch) - Auf jeden Fall. Ich bin hier in den letzten Jahren immer Bestzeiten gelaufen und dazu war meistens 

auch noch super Wetter. Da kommt man natürlich immer wieder gerne!<br /><br /><br />

<i>Welche Eindrücke hast Du ansonsten bisher in Regensburg gesammelt?</i><br /><br />

Ich mag an der Gala auch das ganze Drumherum: Das Stadion liegt schön im Grünen, da kann man 

vor dem Start noch ein bisschen auf der Wiese liegen und runterkommen. Außerdem ist das Catering 

echt lecker, besonders die Schupfnudeln!<br /><br /><br />

<i>Und auf welcher Strecke werden wir Dich dann heuer zu Gesicht bekommen? Gemeldet bist Du ja 

sowohl über die 400m flach, als auch über Deine Paradedisziplin die 400m Hürden.</i><br /><br />

Ich werde die Hürdenstrecke in Angriff nehmen. Ich habe kurz mit den Gedanken gespielt, noch 

einmal ein flaches Rennen zu Saisonbeginn mitzunehmen, aber letztendlich habe ich mich dagegen 

entschieden.<br /><br /><br />

<i>Letztes Jahr hast Du Deine Konkurrenz ja immerhin knapp 1,5 Sekunden hinter Dir gelassen. Nimmst 

Du Dir heuer dasselbe vor?</i><br /><br />

Ich kann meine Form noch nicht so gut einschätzen. Ich führe momentan ein „Doppelleben“ mit 

beruflicher Belastung durch mein Volontariat beim Hessischen Rundfunk zum Einen und der 

Belastung durch den Leistungssport zum Anderen – da kommt man selten mal zur Ruhe. Aber ich 

lasse die Saison jetzt einfach auf mich zukommen und hoffe, dass es wieder so gut läuft wie im 

letzten Jahr.</p>
