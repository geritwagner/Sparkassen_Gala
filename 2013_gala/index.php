<h2>„Eine Seele für die Leichtathletik“</h2>
<strong>Meeting-Direktor investiert in moderne Technik</strong>
<br/><br/>
<p>Vor einem Jahr hätte Kurt Ring fast seine Sachen gepackt. „Wenn der
unter zehn Sekunden läuft, gehe ich nach Hause und schaue mir die Gala
im Livestream an“, sagte der Meeting-Direktor. Der britische
Shooting-Star Adam Gemili brauchte 10,08 Sekunden für die 100 Meter –
und natürlich blieb Ring, der die Gala zusammen mit Jochen Schweitzer
