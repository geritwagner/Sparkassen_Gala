<h2>Die Sparkassen Gala 2013 ist vorbei – danke an alle, die mitgeholfen
haben!</h2>
<strong>Das Unmögliche möglich machen</strong><br/><br/>
<p>Regensburg, 09. Juni 2013 (orv) – Arbeitstechnisch gesehen war
die diesjährige Sparkassen Gala grenzwertig. 1000 Teilnehmer und ein
prall gefülltes Programm im Vorprogramm, im Galaprogramm und in der
Laufnacht haben uns alles abverlangt. Darum verneige ich mich als
Meetingdirektor auch im Namen des Geschäftsführers der
mitveranstaltenden athletics SPORT GmbH Otto Prinzing, meines
Athletenmanagers Jochen Schweitzer und nicht zuletzt unseres
LG-Präsidenten Norbert Lieske vor allen Helfern, Kampfrichtern,
Moderatoren und Technikern, die bei der Sparkassen Gala es möglich
gemacht haben, dass das eigentlich Unmögliche über den ganzen Tag bis
tief in die Nacht hinein möglich werden konnte. Ein dickes Dankeschön
geht auch an alle Athleten/Innen, die wieder einmal ein Feuerwerk von
Topleistungen abschossen. Wir mussten diesmal erkennen, dass bei aller
Liebe zur Leichtathletik das gerade noch Machbare ziemlich ausgeschöpft
war. Deshalb wird es im nächsten Jahr Änderungen geben, die wir mit den
beteiligten Stäben erst einmal eingehend erörtern werden. Mit hoher
Wahrscheinlichkeit wird die Gala dann zweitägig werden, zuerst die
Laufnacht, dann der Rest mit ganz neuen Formaten. Lassen Sie sich ganz
einfach überraschen.<br />
<br /></p>

