        <p>Die 400 m Hürdenläufer konnten zum Regensburger Leistungsboom des letzten Jahres damals weniger beitragen. 50,00 sind für sie immer noch eine harte Nuss. Und trotzdem, für einen Thomas Goller, der im Auf und Ab seiner langen Karriere als ganz Junger 2000 eine 48,81 und dann 2005 noch eine 49,61 lief, sollte das keine Unmöglichkeit sein, auch nicht die 49,26 der WM-Norm, sofern er gesund ist. Konkurrenz mit dem Südafrikaner Woter Le Roux (pB 49,25) hat er jedenfalls. Lars Birger Hense und Thomas Wieser, auch keine Schlechten in der deutschen Langhürdlerszene, wollen auch wieder einsteigen. Vieeleicht kommt auch noch der eine oder andere dazu, mit dem man auf dieser Strecke bisher gar nicht gerechnet hat.</p>
