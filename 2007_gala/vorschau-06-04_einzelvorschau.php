        <p>
          <span style="font-style: italic">4x100 m Männer:</span>
          <br />
          <span style="font-weight: bold">Die DLV-Staffel will die 38,80</span>
        </p>
        <p>Die deutschen Sprinter wollen noch vor dem Europacup in München (23./24. Juni) die Norm über 4x100 Meter für die Weltmeisterschaft in Osaka (Japan; 25. August bis 2. September) erfüllen. Am 9. Juni soll in Regensburg die Vorgabe von 38,80 Sekunden unterboten werden. Am letzten Wochenende lief das Quartett in Weinheim noch knapp an dieser Zeit vorbei. Trotzdem war das Quartett in der Besetzung Tobias Unger (LAZ Salamander Kornwestheim/Ludwigsburg), Ronny Ostwald, Alexander Kosenkow und Sebastian Ernst (alle TV Wattenscheid 01) in 39,04 Sekunden bereits schon flott unterwegs und zeigte nach einem Trainingslager in Monte Gordo (Portugal) aufsteigende Form. "Ich wäre auch schon mit 39,20 Sekunden zufrieden gewesen", lobte Trainer Ronald Stein seine Schützlinge. Nun wolle man am 9. Juni bei der Domspitzmilch-Gala in Regensburg die Norm angreifen.</p>
