            <img src="http://www.lg-regensburg.de/photos/Unger,Tobias1-Gala06-kiefner.jpg" width="151" height="214" align="left" vspace="2" hspace="6" />
          </font>Regensburg, 25. Februar 2007 (orv) –  Der 17. Juni 2006 wird vielen Leichtathleten/Innen in Erinnerung bleiben. Damals, im Regensburger Uni-Stadion lief die deutsche Leichtathletik zur Hochform auf. Dies geschah nicht bei einem der hochgradigen internationalen Topevents, dies geschah bei einem Familienfest der Leichtathletik-Familie. Ein tausend Sportler/Innen waren letztendlich an einem Bilderbuch-Sommertag am Start. Ungewöhnlich, aber nicht gewöhnungsbedürftig. So sollte es immer
          sein.
          <br /> Alle haben sich wohl gefühlt, vom kleinsten Schüler bis zu „Mocki nationale“. Organisatorisch hat alles bestens geklappt, für’s Wetter können wir natürlich keine Garantie beim kommenden Meeting übernehmen. Für den reibungslosen Ablauf aber schon. Die Vorgespräche mit den Trainern und den Topathleten machen uns hoffnungsfroh. Es wird was werden mit der Zweitauflage der Domspitzmilch Gala. „Come together“ heißt das Motto erneut. Je mehr, desto besser, wir werden es packen. Und, bringt die Stimmung des letzten Jahres mit. Sie kann Flügel verleihen, sieben deutsche Jahresbestleistungen bei Männer und Frauen sind am Ende übrig geblieben, sieben weitere im Nachwuchsbereich und unzählige Bestleistungen sprechen eine deutliche Sprache über die Qualität des Vorjahres. Natürlich werden wir uns größte Mühe geben, auch die Manager/Innen und Topathleten/Innen mit entsprechenden Prämien und Servicelistungen zufrieden zu stellen. Eben das geben, was wir können, unsere Bestes.
          <br
          /> So hoffen wir denn auch, dass dieses Gefühl, Leichtathletik pur zu vermitteln, rüber gekommen ist, bei den Zuschauern, bei den Funktionären und allen, die unmittelbar am Wettkampf beteiligt sind. Also auf ein Neues am Samstag, den 9. Juni auf
          den Regensburger Uni-Hügel. </p>
