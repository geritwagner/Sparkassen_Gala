<h3>Startunterlagen/ Startnummern</h3>
<p>  Die Startunterlagen sind in der Regel vereinsweise  gegen Entrichtung der Organisationsgeb&uuml;hren abzuholen. Die Ausgabe von Einzelnummern (&#8222;mein Vereinsboss kommt erst sp&auml;ter zum Bezahlen&#8220; oder &auml;hnliche Begr&uuml;ndungen) sollte die Ausnahme sein.<br />
Die Startnummer ist im Wettkampf unver&auml;ndert auf der Brust zu tragen. Sicherheitsnadeln sind vom Athleten/in selbst mitzubringen. F&uuml;r &#8222;Vergissmeinnichte&#8220; bietet der Ausrichter Viererp&auml;ckchen zum Preis von &euro; 1.- an.<br />
Für sämtliche Läufe ab 800m werden zusätzlich Laufnummern unmittelbar vor dem Start ausgegeben, die unverändert auf der Brust zu tragen sind. Die anderen <span style="font-weight: bold">ausgegebenen Nummern</span> sind <span style="font-weight: bold">hier</span> unverändert <span style="font-weight: bold">auf dem Rücken</span> zu tragen.</p>

<br />

<h3>Startunterlagen wann und wo?</h3>
<p>  Diese gibt’s zu folgenden Zeiten:<br />
Ausgabe 01. Juni 2012 von 16.00 - 18.00 Uhr und 02. Juni 2011 ab 09.00 Uhr vereinsweise gegen Entrichtung der Organisationsgebühren oberhalb der Tribüne des Sportzentrums der Universität.</p>

<br />

<h3>Stellplatz</h3>
<p>  Wir bitten alle Teilnehmer/Innen ihre Stellpatzkarte (alle Wettkämpfe) pünktlich 75 Minuten vor dem Start am Stellplatz (hinter der Tribüne) abzugeben. Bitte die Stellplatzkarten nur abgeben, wenn der Athlet sicher am Wettkampf teilnimmt. Ansonsten gibt es freie Bahnen und das ist unfair gegenüber den anderen Teilnehmern.</p>

<br />

<h3>Callpoint</h3>
<p>  Für die Läufe gibt es einen Callpoint an dem sich die Athleten 15 Minuten vor der Startzeit einfinden müssen. </p>

<br />

<h3>Ergebnisse</h3>
<p>  Die Ergebnisse werden schnellstmöglich nach Beendigung des jeweiligen Wettbewerbes an den dafür vorgesehenen Plätzen (Tribünenbereich) ausgehängt. Wir sind bemüht, die vollständigen Ergebnisse möglichst zeitnah unter der Adresse <a href="http://www.sparkassen-gala.de/" target="_blank">www.sparkassen-gala.de</a> ins Internet zu stellen. </p>

<br />

<h3>Vorsicht vor Dieben</h3>
<p>  Einige unangenehme Individuen unserer Gesellschaft nutzen solche Großveranstaltungen zum Klauen. Da die Umkleidekabinen etwas abseits vom Hauptgeschehen liegen und Diebe in der Anonymität einer größeren Menschenmenge praktisch risikolos ihrem Handwerk nachgehen können, möchten wir Sie bitten, keine Wertgegenstände, wertvolle Bekleidungsstücke oder andere mitnehmenswerte Sachen in den Umkleidekabinen liegen zu lassen. Für Diebstahl und Unfälle jeglicher Art können wir keine Haftung übernehmen.</p>

<br />

<h3>Siegerehrungen</h3>
<p>Siegerehrungen gibt es nur im Vorprogramm der Sparkassen Gala 2011 für die Plätze 1-3. Die Athleten sollen sich 20 Minuten nach Beendigung der Disziplin bereithalten. Die Urkunden (bis Platz 8) können am Stellplatz abgeholt werden.<br />
  Im Hauptprogramm der Gala gibt es keine Siegerehrungen, hier werden Interviews mit den Sieger/Innen durchgeführt.<br />
  <br />
</p>

<h3>Prämien</h3>
<p>Die Prämien (Platz 1-3 im Galaprogramm) werden an der Zahlstelle (im Innenraum auf Höhe des 50m Starts) ausbezahlt.<br />
  <br />
</p>

<h3>Presse und Offizielle</h3>
<p>  Bitte haben Sie Verständnis, dass nicht jedem überall Einlass gewährt wird. Wir sind bemüht, schon im Vorfeld alle Offiziellen und auch alle Presseleute, die sich bei uns und dem DLV gemeldet haben, mit den entsprechenden Akkreditierungen zu versorgen. Sollte der eine oder andere dieses Schildchen noch nicht haben, bitten wir ihn, selbiges beim Infostand anzufordern. Für die Presse steht im Tribünenbereich ein Presseraum mit Infos, Ergebnissen und Internetzugängen zur Verfügung.</p>

<br />

<h3>Parkplätze</h3>
<p>  Es stehen genügend Parkplätze im Umfeld des Sportzentrums bereit. Lassen Sie sich einfach vom Parkdienst einweisen. Nicht ordnungsgemäßes Parken verursacht nur Kosten - und die Regensburger Polizei ist besonders schnell.</p>

<br />

<h3>Information und Reklamation</h3>
<p>  Sollten Sie etwas wissen wollen oder auch etwas beanstanden müssen, wenden Sie sich bitte an die freundliche Dame im Infostand. Diese wird Ihre Sorgen und Nöte notieren, sie weitergeben oder selbst beheben. Sollten Sie wirklich ins Wettkampfbüro müssen, bekommen Sie hier auch den nötigen Ausweis.</p>

<br />

