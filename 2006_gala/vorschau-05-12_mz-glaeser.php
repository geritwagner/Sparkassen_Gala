            <img src="http://www.lg-regensburg.de/images_alt/gala2006cover-gross.jpg" width="152" height="212" align="left" vspace="2" hspace="6">
          </font>Regensburg, 12. Mai 2006 (mz/gläser) - Einige Veranstalter winkten wegen des Termins ab, da ergriff die LG Domspitzmilch ihre Chance. Dank der Tatkraft der Domspitzmilch-Macher kommen die Leichtathletik-Fans in der Region in den Genuss eines
          absoluten sportlichen Highlights. Am 17. Juni - also während der Fußball-WM - steigt im Uni-Stadion die Domspitzmilch-Gala, die gleichzeitig Qualifikation für den Europacup in Malaga (28./29. Juni) sein wird. Stars wie Sprint-Ass Tobias Unger
          und Top-Stabhochspringer Tim Lobinger oder Kollegin Carolin Hingst werden zu der hochkarätigen Veranstaltung in Regensburg erwartet.
          <br> "Die Stadt wird damit bundesweit und sogar darüber hinaus wahrgenommen, kann sich präsentieren und profilieren", sieht Clemens Prokop, der Präsident des Deutschen Leichtathletik-Verbandes (DLV) auch den "Werbewert" dieser Veranstaltung. Zudem
          habe die LG Domspitzmilch "vielfach bewiesen, dass sie Wettkämpfe optimal organisieren kann", lobt der Kelheimer Amtsgerichtsdirektor. Die Vergabe in die Domstadt sei "Ausdruck höchster Wertschätzung und auch ein Motivationsschub für die Leichtathletik
          in Regensburg und Umgebung", sagt der 49-Jährige, der selbst seit langen Jahren LG-Mitglied ist. "Toll, dass sich die LG Domspitzmilch angeboten hat, als andere Meetings wegen der Fußball-WM kein Interesse signalisierten", ergänzt DLV-Mediendirektor
          Peter Schmitt.
          <br> "Regensburg wird absolute Priorität bei der Nominierung der Europacup-Teams haben", bestätigt Siegfried Schonert, der Teammanager der DLV-Nationalmannschaft: "Wir gehen davon aus, dass fast alle unsere Top-Athleten am 17. Juni dabei sein werden."
          Der leitende Bundestrainer Jürgen Mallow habe auf eine Qualifikation gedrungen, die zeitnah zum Europacup in Malaga über die Bühne geht. "Dort wollen wir uns sowohl bei den Männern als auch bei den Frauen für den Weltcup Mitte September in Athen
          qualifizieren", gibt Schonert als ehr-geiziges Ziel für die DLV-Teams aus.
          <br> Der Qualifikation in Regensburg stellen sich die Kurz- und Mittelstreckler sowie die Sprung-Disziplinen - mit Ausnahme der Dreispringer, die ebenso wie die Werfer tags zuvor in Schönebeck (Sachsen-Anhalt) ihre Europacup-Teilnehmer ermitteln.
          Ganz ohne Wurf kommt die Gala aber trotzdem nicht aus. Speerwurf-Bundestrainerin Maria Ritschel nutzt die Gelegenheit für einen Junioren-Norm-Wettbewerb Richtung WM Peking, weil ohnehin alle Europacup-Ausscheidungen zusätzlich die Möglichkeit
          bieten, sich für die Wettbewerbe in der chinesischen Hauptstadt zu qualifizieren.
          <br> Schonert legt im Vorfeld die Latte für die Athleten hoch: "Nur wer einen starken und leistungsfähigen Eindruck macht, ist in Malaga dabei."</p>
