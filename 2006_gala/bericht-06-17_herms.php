            <img src="images/Herms,Rene1.jpg" width="123" height="175" hspace="6" vspace="2" align="left" />Regensburg, 17. Juni 2006 (mz/wotruba) - Früher formulierte Rene Herms seine Ziele schon mal knackig. Zu knackig, wie er selbst findet: "Ich bin vorsichtig geworden nach den Vorhersagen der Vergangenheit." Forsch wollte der 800-Meter-Mann bei Olympia in Athen mit frisch erzielter Best-zeit (1:44,14) etwa vor zwei Jahren ins Finale, geklappt hat's nicht. Auch nicht bei der WM 2005. Und so lautet die Parole für die EM in Göteborg: "Abwarten."
            <br> In Regensburg sorgte Rene Herms mit seinen 1:45,82 Minuten für eine von zwölf deutschen Jahresbestleistungen und blieb auch unter der EM-Norm (1:46,25). Seit 2001 ist der 23-Jährige ununterbrochen und unangefochten Deutscher Meister. Das Comeback von Sydney-Olympiasieger Nils Schumann käme ihm daher durchaus gelegen: "Die Duelle mit Nils waren immer sehr schön. Ich würde mich freuen. Denn, wenn man es drastisch sagen will: Mir fehlt in Deutschland schon die Konkurrenz."
            <br> Drastisch wird Rene Herms aber vor allem, wenn's um die Fußball-WM geht: "Es ist unfair gegenüber den anderen Sportarten, das so ins Rampenlicht zu stellen. Wir haben deswegen nicht die Meetings, die wir bräuchten. Es gibt ja nicht mal Klopapier, auf dem kein Fußball drauf ist." Von den deutschen Kickern ("Die sind ein bisschen überbezahlt") hält er wenig: "Ich mag mehr afrikanische Mannschaften oder Brasilien. Die sind besser.'</font>
