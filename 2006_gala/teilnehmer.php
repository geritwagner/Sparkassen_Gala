          <font color="#FF0000"><b>Fast 1000 Teilnehmer/Innen zwingen zu leichten Veränderungen</b></font>
          <br />
          <br />
          <font face="Arial, Helvetica, sans-serif" size="2">Regensburg, 12. Juni 2006 (orv) - Unser EDV-Mann an der Teilnehmereingabe Thomas Stockmeier hat es geschafft. Am Ende hat er beinahe 1000 Teilnehmer/Innen in den Computer eingetippt. Das hat uns im Vorprogramm zu einigen Veränderungen im Zeitplan
            gezwungen. Das Vorprogramm beginnt jetzt schon um 12.30 Uhr, also eine halbe Stunde früher, mit den 400 m Hürden der weiblichen Jugend. Gravierendste Änderung: Die Jungs und Mädels der B-Jugend laufen ihre 100 m Zeitläufe bereits um 12.50
            Uhr mit den Schüler/Innen. Diese Maßnahme war unumgänglich, weil 20 100 m-Läufe hintereinander einfach nicht machbar wären. Außerdem ist der 1500 m Zeitlauf der mJB wieder ins Vorprogramm gerutscht. Dafür läuft nun die weibliche Jugend A mit
            den Frauen am Abend. Bitte beachten Sie bei den Läufen, dass selbst Athleten/Innen mit qualitativ guten Vorleistungen in die Läufe vom Vorprogramm eingeteilt wurden. Das Galaprogramm ab 16.00 Uhr verkraftet in der Regel immer nur zwei Zeitläufe.
            Meldungen sind nun nur mehr im Ausnahmefall über mich selbst möglich. Im Sprint und in den Weitsprüngen des Vorprogramms geht absolut nichts mehr. Nachmeldungen am Tag selbst entfallen deshalb natürlich auch.Wir bitten all diese Veränderungen
            schon im Voraus zu entschuldigen und versprechen Ihnen am Gala-Tag selbst für einen reibungslosen Ablauf zu sorgen.
            <br />
            <br />gez. Kurt Ring, Organisationsleitung Domspitzmilch Gala 06</p>
