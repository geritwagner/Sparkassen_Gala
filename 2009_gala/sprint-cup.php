    <p class="floatbox">
      <img src="images/inhalt/sprint-cup_wann-wo.gif" alt="Wann? Wo?" width="79" height="125" class="float_left" />Die Sparkasse Regensburg sucht die schnellsten Fünftklass-Pennäler in der Stadt und im Landkreis Regensburg. Dazu wurde der Sprint-Cup der Sparkasse Regensburg ins Leben gerufen. Die Vorendausscheidungen der jeweils 30 bestgemeldeten Mädchen und Jungen findet innerhalb des Vorprogramms der internationalen Sparkassen Gala
      <span style="font-weight: bold">am Sonntag, den 7. Juni 2009 ab 11.15 Uhr im Regensburger Uni-Stadion</span> statt. Die beiden Endläufe mit den jeweils acht schnellsten Regensburger Kids eröffnen das Hauptprogramm der Gala um 14.30 Uhr.</p>
